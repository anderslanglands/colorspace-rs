pub fn uplift_my(rgb: RGBf64) -> VSPD {
    let (my_r, my_g, my_b) = my_basis();
    izip!(my_r.wavelengths(), my_r.values(), my_g.values(), my_b.values())
        .map(|(nm, w_r, w_g, w_b)| Sample::new(nm, w_r * rgb.r + w_g * rgb.g + w_b * rgb.b)).collect()
}

